class Sensor:
    def __init__(self, valor=0):
        self.__valor = valor if 0 <= valor <= 100 else 0

    @property
    def valor(self):
        return self.__valor

    @valor.setter
    def valor(self, nou_valor):
        if 0 <= nou_valor <= 100:
            self.__valor = nou_valor
